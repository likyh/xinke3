		<h2>拾馒头的父亲</h2>
		<h3>一 </h3>
		<p>十六岁那年，我考上了全县最好的高中。听人说，考上这所学校就等于一只脚迈进了大学。父亲欣喜不已，千叮咛万嘱咐，希望我能考上大学，将来坐办公室就不用下地种田了。 </p>
		<p> 恰巧这时，我家在县城的一个亲戚要搬到省城去住，他们想让我父亲去帮忙照看一下房子，还给父亲建议说在县城养猪是条致富路子，因为县城人多，消费水平也高，肯定比农村卖的价钱好。父亲欣然答应，一来这确实是个好法子，二来在县城还可顺便照顾一下我。 </p>
		<p> 等我在高中读了一个学期后，父亲在县城也垒好了猪圈，买来了猪崽。我平时在学校住宿，星期六的时候就去父亲那儿过夜，帮父亲照料一下小猪，好让父亲腾出时间回家去推饲料。 </p>
		<p> 猪渐渐长得大起来，家里的饲料早已被吃了个精光，亲戚送给我们家的饲料也日趋减少。买饲料吧，又拿不出钱来，父亲整日忧心忡忡。 </p>
		<p> 我愁在眉上急在心里，但也一筹莫展。有一天我去食堂打饭时，发现许多同学扔馒头、倒饭菜，我突然想到，把这些东西拾起来喂猪不是挺好吗。 </p>
		<p> 我回去跟父亲一说，父亲高兴得直拍大腿，说真是个好主意，第二天他就去拾馒头、剩饭。 </p>
		<h3>二 </h3>
		<p>我为自己给父亲解决了一个难题而窃喜不已，却未意识到这将给我带来无尽的烦恼。父亲那黑乎乎的头巾、脏兮兮的衣服、粗糙的手立时成为许多同学取笑的对象。他们把诸如&ldquo;丐帮帮主&rdquo;&ldquo;黑橡胶&rdquo;等侮辱性的绰号都加在了父亲头上。 </p>
		<p> 我是一个山村里走出来的孩子，我不怕条件艰苦，不怕跌倒疼痛，却害怕别人的歧视。好在同学们都还不知道那是我的父亲，我也尽量躲避着父亲，每到他来时，我就离得远远的。 </p>
		<p> 但我内心被别人识破和歧视的恐惧却日复一日地剧增。终于有一天我对父亲说：&ldquo;爹，你就别去了，甭叫人家都知道了，会嘲笑我……&rdquo; </p>
		<p> 父亲脸上的喜悦一下子消失了。在漆黑的夜里，只有父亲的烟锅一闪一闪的，良久父亲才说：&ldquo;我还是去吧！不和你打招呼就是了。这些日子，正是猪长膘的时候，不能断了粮的。&rdquo; </p>
		<p> 我的泪禁不住落下来。对不起，父亲，我是真心爱你的，可你偏偏是在学校里拾馒头，我怕被别人看不起呀！ </p>
		<p> 接下来的日子，父亲继续拾他的馒头，我默默地读书，相安无事。我常常看见父亲对着张贴成绩的布告栏发呆，好在我的成绩名列前茅，可以宽慰父亲的，我想。 </p>
		<h3>三 </h3>
		<p>1996年的冬天，我期末考试的成绩排在了年级前三名，而且还发表了许多文章，一下子声名鹊起。班里要开家长会，老师说：&ldquo;让你父亲来一趟。&rdquo; </p>
		<p> 我的心一下子就凉了，我不敢想象别人知道那拾馒头的人就是我父亲后会怎样嘲笑我。伴着漫天风雪回到家，我对父亲说：&ldquo;爹，你就别去了，我对老师说你有病……&rdquo; </p>
		<p> 父亲的脸色很难看，但终究没说什么。 </p>
		<p> 第二天，我冒着风雪冲到了学校，坐在了教室里。家长会开始了，鼓掌声和欢笑声不断，我却一直蔫蔫呆呆，心里冰凉得厉害。父亲啊，你为何偏偏是一个农民，偏偏在我们学校拾馒头呢！ </p>
		<p> 我无心听老师和家长的谈话，随意将目光投向窗外。天哪！父亲，我那拾馒头的父亲正站在教室外面一丝不苟地聆听老师和家长们的谈话，他的黑棉袄上落满了厚厚的积雪。 </p>
		<p> 我的眼泪顿时哗哗地流了下来。我冲出教室，将父亲拉进来，对老师说：&ldquo;这是我爹。&rdquo;一下子掌声雷动…… </p>
		<p> 回去的路上，父亲仍挑着他捡来的两桶馒头和饭菜。父亲说：&ldquo;你其实没必要自卑，别人的歧视都是暂时的，男子汉，只要努力，别人有的，咱们自己也会有。&rdquo; </p>
		<p> 以后，同学们再也没有取笑过父亲，而且都自觉地将剩下的饭菜倒进父亲的大铁桶里。 </p>
		<p> 1997年的金秋，父亲送我来省城读大学。我们乡下人的打扮在绚丽缤纷的校园里显得那么扎眼，但我却心静如水，没有一丝被人嘲笑的忧虑。我明白，在这个世界上，歧视总是难免的，关键是自己要看得起自己。正如父亲说的那样：&ldquo;别人的歧视都是暂时的，男子汉，只要努力，别人有的，咱们自己也会有。&rdquo;</p>
